/**
 * Простой расчёт знаков для натальной карты по дате рождения.
 * Возвращает Солнце (из даты), условную Луну и Асцендент.
 */
function calcNatalChart(birthDate, birthTime) {
  const date  = new Date(birthDate);
  const month = date.getMonth() + 1;
  const day   = date.getDate();

  // Солнечный знак по дате рождения
  const sunBounds = [
    [3,21,'aries'],[4,20,'taurus'],[5,21,'gemini'],[6,21,'cancer'],
    [7,23,'leo'],[8,23,'virgo'],[9,23,'libra'],[10,23,'scorpio'],
    [11,22,'sagittarius'],[12,22,'capricorn'],[1,20,'aquarius'],[2,19,'pisces'],
  ];
  let sunSign = 'aries';
  for (const [m, d, s] of sunBounds) {
    if (month === m && day >= d) { sunSign = s; break; }
    if (month === (m % 12) + 1 && day < d) { sunSign = s; break; }
  }

  // Луна и Асцендент — условный расчёт для демо
  const moonSignIdx = (date.getDate() + date.getMonth() * 3) % 12;
  const ascSignIdx  = birthTime
    ? (parseInt(birthTime.split(':')[0]) * 2) % 12
    : (date.getFullYear() % 12);

  return {
    sun:  sunSign,
    moon: SIGNS[moonSignIdx].id,
    asc:  SIGNS[ascSignIdx].id,
  };
}

