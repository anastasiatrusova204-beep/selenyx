def get_daily_energy() -> dict:
    """Возвращает полную энергию дня — фаза + знак + рекомендации."""
    moon = get_moon_data()
    energy = PHASE_ENERGY.get(moon["phase_name"], PHASE_ENERGY["Растущая Луна"])

    return {
        **moon,
        "intro":        energy["intro"],
        "good":         energy["good"],
        "avoid":        energy["avoid"],
        "tip":          energy["tip"],
    }


def get_moon_data() -> dict:
    """
    Возвращает текущее положение Луны:
    знак, фаза, лунный день и краткие описания.
    """
    now = datetime.now()

    # Подавляем предупреждение о GeoNames — нам не нужны точные координаты для знака Луны
    logging.getLogger("root").setLevel(logging.ERROR)

    subject = AstrologicalSubject(
        "Now",
        now.year, now.month, now.day,
        now.hour, now.minute,
        "Moscow", "RU"
    )

    logging.getLogger("root").setLevel(logging.INFO)

    sign_key = subject.moon.sign          # "Vir"
    moon_lon = subject.moon.abs_pos       # 0–360°
    sun_lon  = subject.sun.abs_pos        # 0–360°

    # Угол между Луной и Солнцем — определяет фазу
    angle = (moon_lon - sun_lon) % 360

    # Определяем фазу по углу
    emoji, phase_name, phase_meaning = "🌙", "Неизвестно", ""
    for threshold, em, name, meaning in PHASES:
        if angle < threshold:
            emoji, phase_name, phase_meaning = em, name, meaning
            break

    # Лунный день (1–30): каждые ~12.19° = 1 день
    lunar_day = int(angle / 12.19) + 1
    lunar_day = min(lunar_day, 30)

    return {
        "sign_key":      sign_key,
        "sign_prep":     SIGNS_RU.get(sign_key, sign_key),      # "в Деве"
        "sign_nom":      SIGNS_RU_NOM.get(sign_key, sign_key),  # "Дева"
        "sign_meaning":  SIGN_MEANING.get(sign_key, ""),
        "phase_emoji":   emoji,
        "phase_name":    phase_name,
        "phase_meaning": phase_meaning,
        "lunar_day":     lunar_day,
        "degree":        round(subject.moon.position, 1),
    }
