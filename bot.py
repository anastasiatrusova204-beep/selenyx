    s2 = sign2.capitalize()
    if s1 == s2:
        return _COMPAT_TABLE["same"]
    elems = tuple(sorted([_SIGN_ELEMENT[s1], _SIGN_ELEMENT[s2]]))
    return _COMPAT_TABLE.get(elems, _COMPAT_TABLE["same"])


